<template>
    <div>
      <router-view :key="$route.fullPath"></router-view>
    </div>
  </template>
  
  <script>
  export default {
    methods: {
      disableRTL() {
        if (!this.$rtl.isRTL) {
          this.$rtl.disableRTL();
        }
      },
    },
    mounted() {
      this.$watch("$route", this.disableRTL, { immediate: true });
    },
  };
  </script>
  
